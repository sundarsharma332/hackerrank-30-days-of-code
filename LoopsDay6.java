import java.util.Scanner;
import java.io.*;

public class LoopsDay6 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();//taking input
		// for loop whhich runs 10 time from index 1 to 10 
		for(int i = 1;i<= 10 ;i++) {
			System.out.println(n + " x " + i + " = " + n * i);// result 
		}

	}

}
// simple loops program in java
